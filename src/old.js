	/**
	 * 緯度経度からブロックとユニットを取得する
	 **/
	function _getBlockUnit(latlng){
		var res = {
			block:null,
			latlng:latlng
		};

		// -------------------------------------------------------
		// Block
		// 南北ブロック
		var nsBlock = null;
		var lat = latlng.lat;
		var nsBlocks = nCode_NS_Blocks.filter(function(element, index, array){
			var max = element.max;
			var min = element.min;
			if(lat >= min && lat < max){
				return true;
			};
			return false;
		});
		if(nsBlocks.length == 1){
			nsBlock = nsBlocks[0];
		}

		// 東西ブロック
		var ewBlock = null;
		var lng = latlng.lng;
		var ewBlocks = nCode_EW_Blocks.filter(function(element, index, array){
			var max = element.max;
			var min = element.min;

			// 180度跨ぎ
			if(min > max){
				if(lng >= min && lng < 180){
					return true;
				}
				if(lng >= -180 && lng < max){
					return true;
				}
				return false;
			}

			if(lng >= min && lng < max){
				return true;
			};
			return false;
		});
		if(ewBlocks.length == 1){
			ewBlock = ewBlocks[0];
		}

		// -------------------------------------------------------
		// Unit
		// 南北ユニット
		var nsUnit = null;
		if(nsBlock){
			var nsUnits = nsBlock.units.filter(function(element, index, array){
				var min = element.min;
				var max = element.max;
				if(lat >= min && lat < max){
					return true;
				};
				return false;
			});
			if(nsUnits.length == 1){
				nsUnit = nsUnits[0];
			}
		}

		// 南北ユニット
		var ewUnit = null;
		if(ewBlock){
			var ewUnits = ewBlock.units.filter(function(element, index, array){
				var min = element.min;
				var max = element.max;
				if(lng >= min && lng < max){
					return true;
				};
				return false;
			});
			if(ewUnits.length == 1){
				ewUnit = ewUnits[0];
			}
		}

		if(nsBlock && ewBlock){
			res.block = {
				name:ewBlock.name + nsBlock.name,
				latMin:nsBlock.min,
				latMax:nsBlock.max,
				lngMin:ewBlock.min,
				lngMax:ewBlock.max,
				unit:null
			}
		};

		if(nsUnit && ewUnit){
			res.block.unit = {
				name:ewUnit.name + nsUnit.name,
				latMin:nsUnit.min,
				latMax:nsUnit.max,
				lngMin:ewUnit.min,
				lngMax:ewUnit.max,
				hMesh:null
			}
		}

		//console.log("_getBlockUnit", res);
		return res;
	};

	/**
	　* 指定した緯度経度範囲のなかで、緯度経度のメッシュを返す
	 **/
	function _getMesh(latlng, latMin, latMax, lngMin, lngMax){
		var res = {
			latName:null,
			lngName:null,
			latMin:NaN,
			latMax:NaN,
			lngMin:NaN,
			lngMax:NaN
		};

		var lat = latlng.lat;
		var lng = latlng.lng;

		for(var i = 0; i < 100; i++){
			var serchLatMin = latMin + (latMax - latMin) * (i / 100);
			var serchLatMax = latMin + (latMax - latMin) * ((i+1) / 100);
			if(lat >= serchLatMin && lat < serchLatMax){
				res.latName = ("0" + (99- i)).slice(-2);
				res.latMin = serchLatMin;
				res.latMax = serchLatMax;
				break;
			}
		}

		var lngMeshMin = null;
		var lngMeshMax = null;
		for(var i = 0; i < 100; i++){
			var serchLngMin = lngMin + (lngMax - lngMin) * (i / 100);
			var serchLngMax = lngMin + (lngMax - lngMin) * ((i+1) / 100);
			if(lng >= serchLngMin && lng < serchLngMax){
				res.lngName = ("0" + i).slice(-2);
				res.lngMin = serchLngMin;
				res.lngMax = serchLngMax;
				break;
			}
		}

		if(res.latName && res.lngName){
			return res;
		}
		return null;
	}

